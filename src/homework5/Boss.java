package homework5;

public class Boss {
    private int health;
    private int damage;
    private String defenceType;

    // Геттер для health
    public int getHealth() {
        return health;
    }

    // Сеттер для health
    public void setHealth(int health) {
        this.health = health;
    }

    // Геттер для damage
    public int getDamage() {
        return damage;
    }

    // Сеттер для damage
    public void setDamage(int damage) {
        this.damage = damage;
    }

    // Геттер для defenceType
    public String getDefenceType() {
        return defenceType;
    }

    // Сеттер для defenceType
    public void setDefenceType(String defenceType) {
        this.defenceType = defenceType;
    }
}
